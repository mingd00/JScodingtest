const plus = (num1, num2) => num1 + num2
const minus = (num1, num2) => num1 - num2
const multi = (num1, num2) => num1 * num2
const quotient = (num1, num2) => Math.floor(num1 / num2)
const remainder = (num1, num2) => num1 % num2

